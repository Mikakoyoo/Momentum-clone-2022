const calculator = {
    add: function(a, b){
        console.log(a + b);
    },
    subtraction: function(a, b){
        console.log(a - b);
    },
    multiplication: function(a, b){
        console.log(a * b);
    },
    division: function(a, b){
        console.log(a / b);
    },
    powerOf: function(a, b){
        console.log(a**b);
    }
};

calculator.add(12, 2);
calculator.subtraction(12, 2);
calculator.multiplication(12, 2);
calculator.division(12, 2);
calculator.powerOf(12, 2);